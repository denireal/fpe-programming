#include <stdio.h>

int main(void)
{
    /* Given values */
    int a = 5;
    int b = 7;
    int c = 9;

    /* Perform arithmetic operations */
    int sum = a + b + c;
    int difference = a - b;

    /* Display the results */
    printf("Sum: %d\n", sum);
    printf("Difference: %d\n", difference);

    return (0);
}
